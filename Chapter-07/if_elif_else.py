#elif statement are used for taking multiple conditions.

number=67
 
if(number>=100):
    print("number is greater than 100")
    
elif(number>70):
    print("number is greater than 70")
    
elif(number>50):
    print("number is greater than 50")
    
elif(number>30):
    print("number is greater than 30")
    
else:
    print("number is less than 30")
    
print("program ended")
     
