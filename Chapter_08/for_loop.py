# ✅For loop with list : -

my_list=[1,22,32,"Rahul","Deepak",67.3,True]

for items in range(len(my_list)):  #it going to lenght of list which is 7
    print(my_list[items])   
    
    
# ✅For loop with tuple : -
my_tuple=(12,10,45,"deepak",4.06,True)

for items in range(len(my_tuple)):
    print(my_tuple[items])
    
    
#✅ for loop with String :-
string="Hello Deepak"
for char in range(len(string)):
    print(string[char])
    
 
