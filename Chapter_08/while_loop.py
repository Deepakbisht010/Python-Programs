# While loop is a condition based loop which is executed whenever the condition is true.
 
num=0
while(num<=10):
    print(num)
    num+=1
    
    
   # ✅While loop with list
l=[1,2,3,4,5]
i=0
while(i<len(l)):
    print(l[i])
    i+=1
    
 #✅While loop with tuple
